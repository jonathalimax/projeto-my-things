    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        if(evt.getActionCommand().equals("Por nome"))
            sql = "SELECT nome, price, description, purchased_year FROM Products WHERE nome LIKE '"+txtByName.getText()+"%'";
        if(evt.getActionCommand().equals("Por preco"))
            sql = "SELECT nome, price, description, purchased_year FROM Products WHERE price = "+txtByPrice.getText();
        if(evt.getActionCommand().equals("Por categoria"))
            if(cbByCategory.getSelectedIndex() != 0)
                sql = "SELECT p.nome, p.price, p.description, p.purchased_year FROM Products p JOIN Category c ON p.id_category = c.id WHERE c.nome LIKE '"+cbByCategory.getSelectedItem()+"'";
        if(evt.getActionCommand().equals("Por subcategoria"))
            if(cbBySubCategory.getSelectedIndex() != 0)
                sql = "SELECT p.nome, p.price, p.description, p.purchased_year FROM Products p JOIN Sub_category sc ON p.id_sub_category = sc.id WHERE sc.nome LIKE'"+cbBySubCategory.getSelectedItem()+"'";
        if(evt.getActionCommand().equals("Por ano"))
            if(cbByYear.getSelectedIndex() != 0)
                sql = "SELECT p.nome, p.price, p.description, p.purchased_year FROM Products p WHERE purchased_year = "+cbByYear.getSelectedItem();
        
      
        if(sql != null) {
            List list = DAO.updateList(sql);
            DML = new DefaultListModel();
                for(int i = 0; i < list.getItemCount(); i++) 
                    DML.addElement(list.getItem(i));
            lstResult.setModel(DML);
        
        }else {
            JOptionPane.showMessageDialog(null, "Campo vazio ou inválido", "Aviso", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

