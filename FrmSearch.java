    private void lstResultMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lstResultMouseClicked
        if(evt.getClickCount() == 2) {
            String sql1 = "SELECT nome, description, price, purchased_year, image1 FROM Products WHERE nome = \""+lstResult.getSelectedValue()+"\"";
            
            try {
                product = DAO.getData(sql1);
            } catch (FileNotFoundException ex) {
                System.err.println(ex);
            } catch (IOException ex) {
                System.out.println(ex);
            }
            
            lblSetNomeResult.setText(product.getNome());
            txtaSetDescResult.setText(product.getDescription());
            lblSetPriceResult.setText(String.valueOf(product.getPrice()));
            lblSetAnoResult.setText(product.getPurchasedYear());
            
            ImageIcon imgIcon= new ImageIcon(product.getImage1());
            Image im = imgIcon.getImage();
            Image myImg = im.getScaledInstance(97, 97, Image.SCALE_SMOOTH);
            ImageIcon newImage = new ImageIcon(myImg);
            lblsetImage1.setIcon(newImage);
        }
    }//GEN-LAST:event_lstResultMouseClicked

